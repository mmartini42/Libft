#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	result;
	int	signes;

	i = 0;
	result = 0;
	signes = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
		signes = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result * signes);
}
